def get_text(key, lang='id', **kwargs):
    """Get translated text for a given key and language."""
    translations = TRANSLATIONS.get(lang, TRANSLATIONS['id'])
    text = translations.get(key, key)
    if kwargs:
        text = text % kwargs
    return text

def build_translator(lang='id'):
    """Return a callable that translates keys for the given language."""
    def _(key, **kwargs):
        return get_text(key, lang, **kwargs)
    return _
